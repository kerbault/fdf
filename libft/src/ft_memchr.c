#include "../inc/libft.h"

void				*ft_memchr(const void *s, int c, size_t n)
{
	while (n--)
	{
		if (*(t_byte *)s != (t_byte)c)
			++s;
		else
			return ((void *)s);
	}
	return (NULL);
}

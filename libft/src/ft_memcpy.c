#include "../inc/libft.h"

void				*ft_memcpy(void *dest, const void *src, size_t n)
{
	t_byte	*ret;

	ret = dest;
	while (n--)
		*(t_byte *)dest++ = *(t_byte *)src++;
	return (ret);
}
